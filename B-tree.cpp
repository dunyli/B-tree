/*
 * B-tree.c - главный файл программы
 * Запускает все тесты и примеры применения
 *
 * Содержит точку входа в программу (main).
 * Выполняет последовательный запуск всех тестов и демонстрационных примеров.
 */

#define _CRT_SECURE_NO_WARNINGS

#include "btree.h"        
#include <stdio.h>        
#include <locale.h>        
 /*
  * Объявления тестовых функций из btree_test.c
  * Они определены в другом файле, поэтому нужно объявить их здесь как extern
  */
extern void test_create_destroy(void);    /* Тест 1: создание и удаление */
extern void test_insert_search(void);     /* Тест 2: вставка и поиск */
extern void test_delete(void);            /* Тест 3: удаление элементов */
extern void test_iterator(void);          /* Тест 4: обход через итератор */
extern void test_large(void);             /* Тест 5: большие объёмы данных */
extern void example_database_index(void); /* Пример 1: индекс БД */
extern void example_sorting(void);        /* Пример 2: сортировка данных */

/*
 * Внешние переменные для статистики тестов
 * Определены в btree_test.c
 */
extern int passed;   /* Количество успешно пройденных тестов */
extern int failed;   /* Количество проваленных тестов */

/*
 * ГЛАВНАЯ ФУНКЦИЯ ПРОГРАММЫ
 *
 * Устанавливает русскую локаль для корректного вывода в консоли,
 * запускает все тесты и примеры, выводит итоговую статистику.
 */
int main(void)
{
    /* Для Windows устанавливаем русскую локаль */
    setlocale(LC_ALL, "Russian");

    /* Выводим заголовок программы */
    printf("\n");
    printf("===============================================================\n");
    printf("                    B-ДЕРЕВО (B-TREE)\n");
    printf("===============================================================\n");
    printf("Сбалансированное дерево поиска\n");
    printf("Все операции: O(log n)\n");
    printf("===============================================================\n");

    /* ============================================================
     * ЗАПУСК ТЕСТОВ
     * ============================================================ */
    printf("\n--- ЗАПУСК ТЕСТОВ ---\n");

    /* Тест 1: Проверка создания и удаления дерева */
    printf("\nТест 1: Создание и удаление\n");
    test_create_destroy();

    /* Тест 2: Проверка вставки и поиска элементов */
    printf("\nТест 2: Вставка и поиск\n");
    test_insert_search();

    /* Тест 3: Проверка удаления с перебалансировкой */
    printf("\nТест 3: Удаление\n");
    test_delete();

    /* Тест 4: Проверка обхода дерева через итератор */
    printf("\nТест 4: Итератор\n");
    test_iterator();

    /* Тест 5: Проверка работы с большим количеством данных */
    printf("\nТест 5: Большие данные\n");
    test_large();

    /* ============================================================
     * ПРИМЕРЫ ПРИМЕНЕНИЯ
     * ============================================================ */
    printf("\n--- ПРИМЕРЫ ПРИМЕНЕНИЯ ---\n");

    /* Пример 1: Использование B-дерева как индекса базы данных */
    printf("\nПример 1: Индекс базы данных\n");
    example_database_index();

    /* Пример 2: Использование B-дерева для сортировки данных */
    printf("\nПример 2: Сортировка данных\n");
    example_sorting();

    /* ============================================================
     * ВЫВОД ИТОГОВ ТЕСТОВ
     * ============================================================ */
    printf("\n===============================================================\n");
    printf("ИТОГИ ТЕСТОВ:\n");
    printf("  УСПЕШНО: %d\n", passed);
    printf("  ПРОВАЛЕНО: %d\n", failed);
    printf("  ВСЕГО: %d\n", passed + failed);

    /* Проверяем, все ли тесты пройдены */
    if (failed == 0) {
        printf("\nВСЕ ТЕСТЫ ПРОЙДЕНЫ УСПЕШНО!\n");
    }
    else {
        printf("\nЕСТЬ ПРОВАЛЕННЫЕ ТЕСТЫ!\n");
    }

    printf("===============================================================\n");
    return 0;  /* Успешное завершение программы */
}